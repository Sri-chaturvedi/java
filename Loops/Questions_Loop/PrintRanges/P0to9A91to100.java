package Loops.Questions_Loop.PrintRanges;

public class P0to9A91to100 {
    public static void main(String[] args) {

        // Print numbers from 0 to 9
        System.out.print("Numbers from 0 to 9: ");
        for (int index = 0; index < 10; index++) {
            System.out.print(index + " ");
        }

        System.out.println(); // move to next line

        // Print numbers from 91 to 100
        System.out.print("Numbers from 91 to 100: ");
        for (int index = 91; index <= 100; index++) {
            System.out.print(index + " ");
        }
    }
}
