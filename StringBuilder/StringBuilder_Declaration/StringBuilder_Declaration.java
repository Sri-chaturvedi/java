package StringBuilder_Declaration;

public class StringBuilder_Declaration {
    public static void main(String[] args) {
        // Creating empty StringBuilder
        StringBuilder sb1 = new StringBuilder();

        // Creating StringBuilder with initial string
        StringBuilder sb2 = new StringBuilder("Hello");

        System.out.println("Empty StringBuilder: " + sb1);
        System.out.println("Initialized StringBuilder: " + sb2);
    }
}
